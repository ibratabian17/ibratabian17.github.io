let currentLang = 'en'; // Default language

function getBrowserLanguage() {
    const lang = navigator.language || navigator.userLanguage;
    if (lang.startsWith('en')) return 'en';
    if (lang.startsWith('id')) return 'id';
    if (lang.startsWith('ar')) return 'ar';
    return 'en'; // Fallback to English
}

function setLanguage(lang) {
    console.log(`Attempting to set language to: ${lang}`);
    currentLang = lang;
    applyTranslations();
}

function applyTranslations() {
    // Update document title
    if (translations[currentLang] && translations[currentLang].title) {
        document.title = translations[currentLang].title;
    }

    const elements = document.querySelectorAll('[data-localize]');
    elements.forEach(element => {
        const key = element.getAttribute('data-localize');
        if (translations[currentLang] && translations[currentLang][key]) {
            // Handle special cases like copyright with dynamic year
            if (key === 'copyright') {
                element.innerHTML = translations[currentLang][key].replace('{year}', new Date().getFullYear());
            } else {
                element.innerHTML = translations[currentLang][key];
            }
        }
    });

    // Handle title attributes for buttons
    const backToTopBtn = document.getElementById('backToTopBtn');
    if (backToTopBtn && translations[currentLang] && translations[currentLang]['back-to-top']) {
        backToTopBtn.title = translations[currentLang]['back-to-top'];
    }

    // Handle RTL for Arabic
    if (currentLang === 'ar') {
        document.body.dir = 'rtl';
    } else {
        document.body.dir = 'ltr';
    }
}

